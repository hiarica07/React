import { toast } from "react-toastify";

export const toastSuccess = () => {
  toast.success("Success!", {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

